p = 0.4;
q = 0.6;

T = [p ^ 2, 0, 0, q ^ 2
     (1-p) ^ 2, 0, 0, q * (1-q)
     p * (1-p), 0, 0, q * (1-q)
     p * (1-p), 1, 1, (1-q) ^ 2];

T_10 = T ^ 10;

P_10 = T_10 * [0, 1, 0, 0]';

fprintf("P_A(10) = %.2f\n", P_10(1));
fprintf("P_B(10) = %.2f\n", P_10(2));
